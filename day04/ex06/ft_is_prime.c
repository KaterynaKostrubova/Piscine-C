int		ft_is_prime(int nb)
{
	int i;

	if ((nb == 2) || (nb == 3))
		return (1);
	if ((nb <= 1) || ((nb % 2) == 0) || ((nb % 3) == 0))
		return (0);
	i = 5;
	while (i < (nb / 2))
	{
		if ((nb % i) == 0)
			return (0);
		i = i + 6;
	}
	i = 7;
	while (i < (nb / 2))
	{
		if ((nb % i) == 0)
			return (0);
		i = i + 6;
	}
	return (1);
}
